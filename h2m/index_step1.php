<h1>Step One: Sanitization </h1>
<p>
Anything that comes in contact with the mead needs to be sanitized beforehand. This is done to encourage the growth and fermentation within the mead. You can do this with a weak bleach solution or another form of sanitized solution such as Easy Clean or Iodophor. Not sanitizing the equipment properly can result in the growth of bacteria that can turn the mead sour, undrinkable, and be harmful to the mead during the fermentation process. Because of this, it is strongly recommended that all bottles, caps/corks, and other equipment used to make the mead be adequately sanitized. Many mead making kits will usually come with one or two sanitization packets to use during the first bottling done with the kit, but after that it will have to be purchased individually.</p> 

<h2><a href="http://homebrewacademy.com/best-homebrewing-sanitizers/">Alternatives</a></h2>
<ul>
<li>Iodophor</li>
<li>One Step</li>
<li>Easy Clean</li>
<li>Star San</li>
<li>BLC Beer Line Cleaner</li>
<li>Saniclean</li>
</ul>
<h2>Procedure</h2>
<p>Place all equipment in the main fermenter bucket and add the sanitization powder that came with the kit. Add water and let the equipment soak for the specified time on the packet. After that, empty the fermenter and begin the mead making process. The sanitization powder that comes with the kit may need to be rinsed out of the fermenter and equipment while other kinds may not harm the mead in anyway. For other sanitization methods, you should check the back of the packet first before using just in case. </p>
