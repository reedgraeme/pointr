<h2>Background</h2>
<p>Mead also known as “honey wine” is an alcoholic beverage consisting of water, honey and yeast. Sometimes mead is also fermented with various fruits and spices. Mead has been known to originate from many different sources including Africa, Europe and Asia. Mead has a wide variety of flavors to chose from depending on the amount and type of honey used, the additives included such as apples, vanilla and cinnamon, and the amount of time the mead is left to ferment for.</p>
<h2>Popular varieties of mead</h2>
<ul>
<li>Sweet Mead- Usually consists of just honey</li>
<li>Bochet Mead- A caramelized honey version usually with chocolate or caramel</li>
<li>Cyser Mead- Includes apple juice or sliced apples</li>
<li>Bilbemel Mead- A blueberry flavored mead, usually by the use of blueberries or blueberry juice</li>
</ul>
