<h1>Equipment Overview</h1>
<h2>"Making Great Mead Requires Great Equipment"</h2>

<p><h1><a href="http://www.northernbrewer.com/winemaking/wine-equipment/wine-fermenting-equipment/buckets">Fermenter Bucket</a></h1>-A food grade bucket with volume graduations, measured in gallons. Make sure when you purchase the bucket that the lid clamps down, unlike mead buckets, wine bucket lids do not clamp down they simply rest on the top of the bucket, this can be confusing because the two buckets look exactly the same.</p>

<p><h1><a href="http://www.northernbrewer.com/brewing/brewing-equipment/fermenting-equipment/carboys-jugs/1-gallon-jug">Carboy Container</a></h1>- Can come either as glass or plastic, this is used to store the mead for the second stage of fermentation, after it is done the main stage of fermenting in the fermenter bucket.</p>

<p><h1><a href="http://www.midwestsupplies.com/airlock-s-bubble-type.html">Bubble Air Lock</a></h1>- Placed on the lid of the fermenter bucket and carboy. This acts as a seal so no air comes in contact with the mead while it is fermenting. Fill the lock half full with water, this causes no air to enter while also allowing excess air to escape the fermenter.</p>

<p><h1><a href="http://meadist.com/making-mead/mead-tips-tricks/best-yeast-for-mead-making/">Yeast Varieties</a></h1>- While making mead one may assume that any yeast will do, however there are specific types of yeast that are specially made just for mead, that speed up the fermenting process. A good variety of yeast to use is “lalvin D-47”</p>

<p><h1>Purified Water</h1>- It is recommended that you use purified water rather than tap water, because the chemicals in regular water can affect the fermentation process.</p>

