<h1>Step Three: Transfer</h1>
<p>Now that three months have past, remove the lid from the mead. Prepare your carboy container and the syphon by sanitizing them with the same sanitizer used in step 1. Place one end of the syphon in the fermenter bucket and begin pumping the mead from the fermenter bucket into the carboy container. While doing this be careful not to scrape the bottom of the fermenter bucket because all of the dead yeast has settled to the bottom of the bucket by now. We do not want any yeast to make it into the carboy container. </p>

<h2>Sealing</h2>
<p>Now that all of the mead is in the carboy container, cork the bottle with either a regular cork or a hollow cork that fits the bubble airlock. By using the hollow cork with the bubble lock it allows the mead to breath just in case some yeast traces made it into the carboy container. If some traces did make it into the carboy and you used the regular cork, you run the risk of the bottle exploding because the yeast will breathe and won’t be able to leave the bottle.</p>

<h2>Time</h2>
<p>Let the mead sit for as long as you feel is necessary. The recommended amount of time is one year, however, if you can't wait, it doesn’t really matter that much. Letting the mead sit for a longer amount of time just enhances the flavour.</p>

<h2>Finally, Enjoy!</h2>
