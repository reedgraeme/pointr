<h1>Step Two: Prepare Supplies</h1>

<p>Once all of the equipment is sanitized begin warming the honey (you can do this by placing the bottles in a pot on the stove, fill the pot half full with water, put the pot on low heat). Warmed honey is easier to mix in with the water. Take the fermenter bucket and fill it with the specified amount of water relative to the size of the fermenter bucket purchased, usually around 1/3 of the bucket. Once the honey has been warmed, pour it into the fermenter water, mix until the water and honey are completely blended together. </p>
<h2>Yeast</h2>
<p>Take a small amount of water from the tap and heat it to the specified temperature listed on the yeast packet. Once the water is heated. Stir the yeast into the warm water until the yeast is fully activated. Add the yeast mixture into the water/honey mixture and stir the water/honey/yeast mixture together.</p>
<h2>Mixing & Storage</h2>
<p>Now that everything is mixed, put the lid on top of the bucket. Take the bubble cork weight and fill it half full with water. Force it into the hole on the top of the lid of the fermenter bucket making sure that no air can get into or out of the bucket. Mark the lid of the mead with a date three months from the current date and wait until then. Let the fermenter bucket sit in an area that is room temperature. If the bucket sits somewhere too cold you run the risk of slowing the fermentation process.</p>
